a=input("enter name:> ")
b=int(len(a))
c=int(len(a)/2)
if b%2!=0:
    n=a[0:(c)]
    m=a[(c+1):(b)]
    for j in n:
        j in n
    if n==m:
        print("it is Laplindrome")
    else:
        print("it is not Laplindrome")
else:
    n=a[0:(c)]
    m=a[(c):(b)]
    for j in m: 
        j in m
    if n==m:
        print("it is Laplindrome")
